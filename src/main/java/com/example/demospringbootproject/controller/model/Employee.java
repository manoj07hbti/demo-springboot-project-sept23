package com.example.demospringbootproject.controller.model;

public class Employee {
    String name;
    int id;
    int age;
    String dept;

    public Employee(String name, int id, int age, String dept) {
        this.name = name;
        this.id = id;
        this.age = age;
        this.dept = dept;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }
}